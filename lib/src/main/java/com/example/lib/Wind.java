package com.example.lib;

public class Wind {
    public Float speed;
    public Float degree;

    public void setSpeed(Float speed){
        this.speed = speed;
    }

    public Float getSpeed(){
        return speed;
    }

    public void setDegree(Float degree){
        this.degree = degree;
    }

    public Float getDegree(){
        return degree;
    }
}
