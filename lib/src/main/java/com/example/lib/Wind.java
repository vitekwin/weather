package com.example.lib;

public class Wind {
    private Double speed = 2.57;
    private Double degree = 295.003;

    public void setSpeed(Double speed){
        this.speed = speed;
    }

    public Double getSpeed(){
        return speed;
    }

    public void setDegree(Double degree){
        this.degree = degree;
    }

    public Double getDegree(){
        return degree;
    }
}
